    
# Arthemetic Operators ( + , - , * , / , % , **)

a = 5
b = 6
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)

# Relation/Comparision Operators ( == , != , < , > , <= , >= )

a = 50
b = 20
print( a == b)
print( a != b)
print( a < b)
print( a > b)
print( a <= b)
print( a >= b)

#Assignment operators ( = , += , -= , *= , /= , %= , **=  )

